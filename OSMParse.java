	public static int numEntries(String[] osm, String type) {
      int i;
      int length = osm.length;
      int count = 0;
      type = "<" + type;
      for(i = 0; i < length; i++){
         if(osm[i].length() > type.length()){
            if (osm[i].indexOf(type) != -1) {
               count ++;
            } 
          }           
      } 
		return count;
	}

	/*
	 * type: should be one of either "node", "bounds", or "way".
	 * idx is the nth instance of the item that you want of the
	 * specific type.
	 */
    
	public static int nthEntry(String[] osm, String type, int idx) {
      //2 indexes: one being passed in (count of node), one is index into array of lines in the file
      int length = osm.length;
      int i = 0;
      int count = 0;
      type = "<" + type;
      for(i = 0; i < length; i++){
         if(osm[i].length() > type.length()){
            if (osm[i].indexOf(type) != -1){//index of where type starts; -1 else
               if (count == idx){
                  return i;
               } 
                 
               count ++;
            } 
          } 
      }
		return -1;
	}

	/*
	 * Look for a x="y" pair, where x is name, and y is returned.
	 * The empty string is returned if the attribute isn't found.
	 */
    
	public static String getAttribute(String s, String name) { //can use whatever tag you need
      int lengthOfS = s.length();
      int lengthOfName = name.length();
      int i = 0;
      for(i = 0; i < lengthOfS; i++){
         if(lengthOfS > lengthOfName){
            if(s.substring(i, i + lengthOfName).equals(name)){
               int beginning = i + lengthOfName + 2;
               String newString = s.substring(beginning, lengthOfS);
               int end = newString.indexOf("\"");
               String value = newString.substring(0, end);
               return value;
            }  
         }
      }            
      return "";
	}

	/*
	 * return a 3 length array with the longitude, latitude, and
	 * node id.
	 */
    
	public static double[] nodeInfo(String[] osm, int idx) {
      int numberOfNodes = numEntries(osm, "node ");
      int lineOfIdx = nthEntry(osm, "node ", idx);
      //String a = "node";
      //int c = a.length();
      if(lineOfIdx >= 0){
         if(idx < numberOfNodes){
            double lon = Double.parseDouble(getAttribute(osm[lineOfIdx], "lon"));
            double lat = Double.parseDouble(getAttribute(osm[lineOfIdx], "lat"));
            double nodeid = Double.parseDouble(getAttribute(osm[lineOfIdx], "id"));
            double[] nodeInfo = {lon, lat, nodeid};
            //System.out.println(lon);
            //System.out.println(lat);
            //System.out.println(nodeid);
            
            return nodeInfo;
         }   
      }
		return new double[]{-1,-1,-1};
	}
   
